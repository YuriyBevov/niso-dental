<h2><strong>
Как получить налоговый вычет за лечение зубов в клинике «НИСО» </strong></h2>
<p>
	 Если вы лечите зубы или проходите протезирование в нашей клинике «НИСО» в Санкт-Петербурге, вы можете вернуть часть потраченных средств с помощью социального налогового вычета. Государство возвращает 13% от суммы, которую вы оплатили за стоматологические услуги. Это хороший способ сэкономить и вернуть деньги в семейный бюджет.
</p>
<p>
	 Вот как это сделать:
</p>
<p>
	 1. Соберите необходимые документы<br>
	 Для оформления налогового вычета вам понадобятся:
</p>
<ul>
	<li>
	паспорт (копия первой страницы); </li>
	<li>
	ИНН; </li>
	<li>
	договор на оказание стоматологических услуг, заключенный с нашей клиникой; </li>
	<li>
	кассовые чеки или квитанции об оплате; </li>
	<li>
	лицензия клиники «НИСО» (мы предоставим копию); </li>
	<li>
	справка об оплате медицинских услуг (по форме, установленной ФНС). </li>
</ul>
<p>
	 Все эти документы вы можете получить у нас в клинике — просто сообщите администратору, что вам нужна справка для налогового вычета. Мы подготовим ее в течение нескольких дней.
</p>
<p>
	 2. Подайте декларацию 3-НДФЛ в налоговую инспекцию<br>
	 Вы можете подать декларацию лично в налоговой инспекции, отправить ее по почте или воспользоваться онлайн-сервисом «Личный кабинет налогоплательщика» на сайте ФНС.
</p>
<p>
	 3. Получите возврат 13% от суммы лечения<br>
	 После подачи декларации налоговая проверит документы и в течение 3 месяцев перечислит вам на счет сумму возврата — 13% от всех ваших расходов на лечение.
</p>
<p>
	 Что важно знать:
</p>
<ol>
	<li>
	Максимальная сумма, с которой можно получить вычет, — 120 000 рублей в год (то есть вернуть можно до 15 600 рублей). </li>
	<li>
	Если вы лечите зубы у ребенка или родственников (супруга, родителей), то также можете оформить вычет за них. </li>
	<li>
	Вычет предоставляется только при оплате лечения из собственных средств (не за счет работодателя или других программ). </li>
</ol>
<p>
	 В клинике «НИСО» мы всегда готовы помочь вам собрать все необходимые документы. Наши администраторы подробно расскажут, что именно нужно для оформления вычета, и при необходимости сделают все копии и справки.
</p>
<blockquote>
	 Если у вас есть вопросы или вы хотите узнать больше, просто свяжитесь с нами — мы всегда готовы помочь сделать стоматологическое лечение максимально выгодным и комфортным для вас!
</blockquote>
