<h2><strong>Беспроцентная рассрочка в клинике «НИСО»</strong></h2>
<p>
	 Мы в клинике «НИСО» знаем, что красивая и здоровая улыбка — это важная часть уверенности в себе. Но мы также понимаем, что лечение зубов может стать серьезной финансовой нагрузкой. Чтобы сделать заботу о здоровье доступной для каждого, мы предлагаем своим пациентам удобную возможность — оплату лечения в беспроцентную рассрочку.
</p>
<p>
	 Это отличный способ начать лечение уже сегодня, не откладывая его на потом. Вы можете спокойно получать необходимые стоматологические услуги, а платить — равными частями, без лишних переплат.
</p>
<h2><strong>Условия рассрочки</strong></h2>
<ol>
	<li>0% переплат — вы платите ровно ту сумму, которая указана в плане лечения.</li>
	<li>Гибкие сроки — обычно рассрочка предоставляется сроком от 3 до 12 месяцев.</li>
	<li>Доступно для всех — мы не требуем большого пакета документов или справок о доходах.</li>
	<li>Прозрачность — никаких скрытых комиссий и сложных условий.</li>
</ol>
<h2><strong>Услуги, доступные в рассрочку</strong></h2>
<ul>
	<li>
	Ортодонтическое лечение: брекет-системы и элайнеры. </li>
	<li>
	Имплантация зубов. </li>
	<li>
	Имплантация по протоколам All-on-4 и All-on-6. </li>
	<li>
	Протезирование — виниры и циркониевые коронки. </li>
</ul>
<h2><strong>Как оформить рассрочку?</strong></h2>
<ol>
	<li>На первичной консультации врач составит для вас индивидуальный план лечения и рассчитает его стоимость.</li>
	<li>Администратор подробно расскажет обо всех условиях рассрочки и поможет выбрать наиболее удобный для вас срок.</li>
	<li>Вы подписываете договор на лечение и соглашение о рассрочке — это занимает всего несколько минут.</li>
	<li>Лечение начинается сразу, а платить вы начинаете с удобной для вас даты.</li>
</ol>
